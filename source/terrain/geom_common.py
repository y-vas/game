################################################################################
class CCommonVertex:
    #---------------------------------------------------------------------------
    def __init__(self, x, y):
        self.x = x
        self.y = y

################################################################################
class CCommonRect:
    #---------------------------------------------------------------------------
    def __init__(self, x, y, width, height):
        self.v = CCommonVertex(x, y)
        self.width = width
        self.height = height

################################################################################
class CRect:
    #---------------------------------------------------------------------------
    def __init__(self, x, y, width, height):
        self.x = x
        self.y = y
        self.width = width
        self.height = height
