class CVertexData:
    #---------------------------------------------------------------------------
    def __init__(self):
        self.Reset()

    #---------------------------------------------------------------------------
    def Reset(self):
        self.in_new_mesh = 0 # True if vertex is moved to new mesh

################################################################################
class CVertex:
    #---------------------------------------------------------------------------
    cnt = 0

    #---------------------------------------------------------------------------
    def __init__(self, x = 0, y = 0):
        # Coordinates
        self.x = x
        self.y = y

        # Id
        self.id = CVertex.cnt
        CVertex.cnt += 1

        # Misc
        self.data = CVertexData()

    #---------------------------------------------------------------------------
    def IsEqual(self, v):
        if self.x == v.x and self.y == v.y:
            return 1
        else:
            return 0

################################################################################
class CVariableVertex:
    #---------------------------------------------------------------------------
    def __init__(self, present = 0, v = CVertex()):
        self.present = present
        self.v = v

